import mongoose from "mongoose";

const DonosSchema = new mongoose.Schema({
  nome: { type: String, required: true },
  email: { type: String, unique: true, required: true },
  senha: { type: String, required: true },
  pets: { type: Array, immutable: true },
  createdAt: {
    type: Date,
    default: () => Date.now(),
    immutable: true,
  },
  updatedAt: {
    type: Date,
    default: () => Date.now(),
  },
});

const Donos = mongoose.model("donos", DonosSchema);

export { Donos, DonosSchema };
